package com.kh.pratice.point.model.vo;

public class Point {
    int x;
    int y;

    public Point() {
        x = 2;
        y = 4;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public String toString(){
        return toString();
    }



}
