package com.kh.pratice.point.model.vo;

public class Rectangle extends Point {
    int width;
    int height;

    public Rectangle() {
        width = 10;
        height = 10;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public String toString() {
        return toString();
    }
}
