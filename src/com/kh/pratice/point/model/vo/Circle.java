package com.kh.pratice.point.model.vo;

public class Circle extends Point {
    int radius;

    public Circle() {
        super();
        radius = 5;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

    public String toString() {
        return toString();
    }

}
